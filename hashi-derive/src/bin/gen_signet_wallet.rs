//! Generate a fresh signet wallet: WIF private key + P2WPKH + P2TR addresses.
//!
//! Use this to fund a Bitcoin signet test wallet you control (not the
//! Hashi-derived deposit address). With the WIF you can sign and broadcast
//! signet transactions — including a deposit TX to the Hashi vault for
//! end-to-end testing of the bridge.
//!
//! Usage:
//!   cargo run --quiet -p hashi-derive --bin gen-signet-wallet
//!
//! Output is printed; save the WIF somewhere safe. Recommended:
//!   ./hashi-derive/target/.../gen-signet-wallet > .env.btc-signet
//!   chmod 600 .env.btc-signet

use bitcoin::{
    secp256k1::{rand, Secp256k1},
    Address, CompressedPublicKey, Network, PrivateKey, XOnlyPublicKey,
};

fn main() -> anyhow::Result<()> {
    let secp = Secp256k1::new();
    let (sk, pk) = secp.generate_keypair(&mut rand::thread_rng());

    let priv_key = PrivateKey::new(sk, Network::Signet);
    let wif = priv_key.to_wif();

    let compressed = CompressedPublicKey::from_private_key(&secp, &priv_key)
        .map_err(|e| anyhow::anyhow!("compressed pubkey: {}", e))?;
    let p2wpkh = Address::p2wpkh(&compressed, Network::Signet);

    let keypair = bitcoin::secp256k1::Keypair::from_secret_key(&secp, &sk);
    let (xonly, _parity) = XOnlyPublicKey::from_keypair(&keypair);
    let p2tr = Address::p2tr(&secp, xonly, None, Network::Signet);

    let pk_hex: String = pk.serialize().iter().map(|b| format!("{:02x}", b)).collect();
    let xo_hex: String = xonly.serialize().iter().map(|b| format!("{:02x}", b)).collect();

    println!("# Bitcoin signet test wallet — KEEP THE WIF PRIVATE.");
    println!("# Generated by hashi-derive/gen-signet-wallet.");
    println!("BTC_SIGNET_WIF={}", wif);
    println!("BTC_SIGNET_PUBKEY_COMPRESSED={}", pk_hex);
    println!("BTC_SIGNET_XONLY={}", xo_hex);
    println!("BTC_SIGNET_P2WPKH={}", p2wpkh);
    println!("BTC_SIGNET_P2TR={}", p2tr);
    Ok(())
}
